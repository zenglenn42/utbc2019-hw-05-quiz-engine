//--------------------------------------------------------------------------//
// Mutates a large chunk of themed quiz json into an object which can       //
// create a normalized set of quiz items.                                   //
//                                                                          //
// The flagship method for this object is QuizFactory.createQuiz().         //
//--------------------------------------------------------------------------//

function QuizFactory() {}
QuizFactory.prototype.quizzes = QuizCatalog;
QuizFactory.prototype.getQuizKeys = function() {
    var list = [];
    for (let key in this.quizzes) {
        list.push(key);
    }
    list.sort();
    return list;
}
QuizFactory.prototype.swalQuizSelect = function() {
    results = {};
    let keys = this.getQuizKeys();
    results.inputOptions = {};
    for (let key of keys) {
        results.inputOptions[`${key}`] = key;
    }
    return results;
}
QuizFactory.prototype.getQuizJSON = function(quizKey) {
    return this.quizzes[quizKey];
}
QuizFactory.prototype.createQuiz = function(quizKey, numItems = 10) {
    let quiz = undefined;
    let imgSrc = "";
    let praise = undefined;
    let quizItems = undefined;
    if (this.quizzes.hasOwnProperty(quizKey)) {
        let quizJson = this.getQuizJSON(quizKey);
        console.log("createQuiz: quizJson ", quizJson);
        if (quizJson.hasOwnProperty("schemaType")) {
            let schemaType = quizJson.schemaType;
            switch(schemaType) {
                case "OpenTDB":
                    quizItems = quizJson.quiz.results;
                    break;
                case "UTBC":
                    quizItems = quizJson.quiz.questions;
                    break;
                default:
                    console.warn("QuizFactory.createQuiz: Unsupported schemaType ", schemaType);
            }
            if (quizItems) {
                imgSrc = this.getImgSrc(quizJson);
                praise = this.getPraise(quizJson);
                //--------------------------------//
                // Instantiate a new quiz object. //
                //--------------------------------//
                quiz = new NormalizedQuiz(schemaType, quizKey, quizItems, imgSrc, praise, numItems);
            }
        } else {
            console.warn("QuizFactory.createQuiz: unknown quiz: ", quizKey);
        }
    }
    return quiz;
}
QuizFactory.prototype.getPraise = function(quizJson) {
    let praise = undefined;
    if (quizJson.hasOwnProperty("praise")) {
        praise = quizJson.praise
    }
    return praise;
}
QuizFactory.prototype.getImgSrc = function(quizJson) {
    let imgSrc = "";
    if (quizJson.hasOwnProperty("imgSrc")) {
        imgSrc = quizJson.imgSrc;
    }
    return imgSrc;
}

//--------------------------------------------------------------------------//
// This class takes in quizzes of various schema types and normalizes them  //
// so consumption by clients is simplified down to one interface.           //
//--------------------------------------------------------------------------//

function NormalizedQuiz(schemaType, quizName, quizItems, imgSrc, praise, numItems) {
    this.quizName = quizName;
    this.quizItems = this.normalizeSchema(schemaType, quizItems);
    this.imgSrc = (imgSrc) ? imgSrc : this.defaultImgSrc;
    this.praise = (praise) ? praise : this.defaultPraise;
    if (numItems) {
        this.numItems = (numItems < this.quizItems.length) ? numItems : this.quizItems.length;
    } else {
        this.numItems = this.quizItems.length;
    }
    this.quizItems.length = this.numItems;
    this.index = 0;
    this.shuffleOnReset = true;
}
NormalizedQuiz.prototype.defaultPraise = [
    {"text": "Nice job."},
    {"text": "Like a boss."},
    {"text": "You're so money."},
    {"text": "And you're sooo good looking."},
    {"text": "Sweet"},
    {"text": "You make this look easy."},
    {"text": "Correct"},
    {"text": "Chuck Norris thinks you're a badass."},
    {"text": "Whoa, dude :-)"},
    {"text": "So good."},
    {"text": "Right"}
];
NormalizedQuiz.prototype.defaultImgSrc = "assets/img/default-theme.jgp";
NormalizedQuiz.prototype.getImgSrc = function() {
    return this.imgSrc;
}
NormalizedQuiz.prototype.getRandomPraise = function() {
    let i = Math.floor(Math.random() * this.praise.length);
    return this.praise[i].text;
}
NormalizedQuiz.prototype.hasMoreItems = function() {
    return (this.index < this.quizItems.length);
}
NormalizedQuiz.prototype.getNextItem = function() {
    // One day I'll be a real iterator. :-)
    return (this.quizItems[this.index++]);
}
NormalizedQuiz.prototype.reset = function() {
    this.index = 0;
    if (this.shuffleOnReset) {
        this.shuffleItems();
    }
}
NormalizedQuiz.prototype.getName = function() {
    return this.quizName;
}
//
// NB: Add new quiz schema types by extending this method.  UTBC is the
//     target schema coming out of this method.
//
NormalizedQuiz.prototype.normalizeSchema = function(schemaType, quizItems) {
    results = [];
    switch(schemaType) {
        case "UTBC":
            return quizItems;
            break;
        case "OpenTDB":
            results = quizItems.map(item => {
                let normedItem = {};
                normedItem.question = {}
                normedItem.question.text = item.question;
                normedItem.choices = item.incorrect_answers.map(ia => {
                    let choice = {};
                    choice.text = ia;
                    return choice;
                });
                // Insert the answer into the array of choices at some
                // random location.  But keep track of that location.
                normedItem.answerIndex = Math.floor(Math.random() * (normedItem.choices.length+1));
                let answerItem = {"text": item.correct_answer};
                normedItem.choices.splice(normedItem.answerIndex, 0, answerItem);
                return normedItem;
            });
            return results;
            break;
        //case "Quizlet"
        //  break;
        default:
            console.log("NormalizedQuiz.normalizeSchema unsupported schema: ", schemaType);
    }
}
NormalizedQuiz.prototype.shuffleItems = function() {
    // this.reset();   // shuffling implies reset

    // This is the Fisher-Yates shuffle.
    // https://en.wikipedia.org/wiki/Fisher-Yates_shuffle
    for (let i = this.quizItems.length - 1; i > 0; i--) {
        let j = Math.floor(Math.random() * (i + 1)); // random index from 0 to i
        [this.quizItems[i], this.quizItems[j]] = [this.quizItems[j], this.quizItems[i]]; // swap elements
    }
}
NormalizedQuiz.prototype.getQuestionHtml = function(quizItem) {
    let result = undefined;
    // console.log("qi = ", quizItem);
    // console.log("qi.question = ", quizItem.question);
    // console.log("qi.question.text = ", quizItem.question.text);
    result = `<span class="question">${quizItem.question.text}</span>`;
    return result;
}
NormalizedQuiz.prototype.getChoicesHtml = function(quizItem) {
    let results = "";
    let choices = quizItem.choices;
    let answerIndex = quizItem.answerIndex;
    for (let ci in choices) {
        let choiceHtml = undefined;
        if (ci == answerIndex) {
            choiceHtml = `<button id="answer" class="btn btn-choice btn-primary">${choices[ci].text}</button>`;
        } else {
            choiceHtml = `<button class="btn btn-choice btn-primary">${choices[ci].text}</button>`;
        }
        results += `<p class="para-choice">${choiceHtml}</p>`;
    }
    return results;
}

