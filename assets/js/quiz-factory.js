NormalizedQuiz.prototype.normalizeSchema = function(schemaType, quizItems) {
    results = [];
    switch(schemaType) {
        case "UTBC":
            return quizItems;
            break;
        case "OpenTDB":
            results = quizItems.map(item => {
                let normedItem = {};
                normedItem.question = {}
                normedItem.question.text = item.question;
                normedItem.choices = item.incorrect_answers.map(ia => {
                    let choice = {};
                    choice.text = ia;
                    return choice;
                });
                // Insert the answer into the array of choices at some
                // random location.  But keep track of that location.
                normedItem.answerIndex = Math.floor(Math.random() * (normedItem.choices.length+1));
                let answerItem = {"text": item.correct_answer};
                normedItem.choices.splice(normedItem.answerIndex, 0, answerItem);
                return normedItem;
            });
            return results;
            break;
        //case "Quizlet"
        //  break;
        default:
            console.log("NormalizedQuiz.normalizeSchema unsupported schema: ", schemaType);
    }
}
NormalizedQuiz.prototype.shuffleItems = function() {
    // this.reset();   // shuffling implies reset

    // This is the Fisher-Yates shuffle.
    // https://en.wikipedia.org/wiki/Fisher-Yates_shuffle
    for (let i = this.quizItems.length - 1; i > 0; i--) {
        let j = Math.floor(Math.random() * (i + 1)); // random index from 0 to i
        [this.quizItems[i], this.quizItems[j]] = [this.quizItems[j], this.quizItems[i]]; // swap elements
    }
}
NormalizedQuiz.prototype.getQuestionHtml = function(quizItem) {
    let result = undefined;
    // console.log("qi = ", quizItem);
    // console.log("qi.question = ", quizItem.question);
    // console.log("qi.question.text = ", quizItem.question.text);
    result = `<span class="question">${quizItem.question.text}</span>`;
    return result;
}
NormalizedQuiz.prototype.getChoicesHtml = function(quizItem) {
    let results = "";
    let choices = quizItem.choices;
    let answerIndex = quizItem.answerIndex;
    for (let ci in choices) {
        let choiceHtml = undefined;
        if (ci == answerIndex) {
            choiceHtml = `<button id="answer" class="btn btn-choice btn-primary">${choices[ci].text}</button>`;
        } else {
            choiceHtml = `<button class="btn btn-choice btn-primary">${choices[ci].text}</button>`;
        }
        results += `<p class="para-choice">${choiceHtml}</p>`;
    }
    return results;
}

