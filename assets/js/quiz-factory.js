//--------------------------------------------------------------------------//
// Mutates a large chunk of themed quiz json into an object which can       //
// create a normalized set of quiz items.                                   //
//                                                                          //
// The flagship method for this object is QuizFactory.createQuiz().         //
//--------------------------------------------------------------------------//

function QuizFactory() {}
QuizFactory.prototype.quizzes = QuizCatalog;
QuizFactory.prototype.getQuizKeys = function() {
    var list = [];
    for (let key in this.quizzes) {
        list.push(key);
    }
    list.sort();
    return list;
}
QuizFactory.prototype.swalQuizSelect = function() {
    results = {};
    let keys = this.getQuizKeys();
    results.inputOptions = {};
    for (let key of keys) {
        results.inputOptions[`${key}`] = key;
    }
    return results;
}
QuizFactory.prototype.getQuizJSON = function(quizKey) {
    return this.quizzes[quizKey];
}
QuizFactory.prototype.createQuiz = function(quizKey, numItems = 10) {
    let quiz = undefined;
    let imgSrc = "";
    let praise = undefined;
    let quizItems = undefined;
    if (this.quizzes.hasOwnProperty(quizKey)) {
        let quizJson = this.getQuizJSON(quizKey);
        console.log("createQuiz: quizJson ", quizJson);
        if (quizJson.hasOwnProperty("schemaType")) {
            let schemaType = quizJson.schemaType;
            switch(schemaType) {
                case "OpenTDB":
                    quizItems = quizJson.quiz.results;
                    break;
                case "UTBC":
                    quizItems = quizJson.quiz.questions;
                    break;
                default:
                    console.warn("QuizFactory.createQuiz: Unsupported schemaType ", schemaType);
            }
            if (quizItems) {
                imgSrc = this.getImgSrc(quizJson);
                praise = this.getPraise(quizJson);
                //--------------------------------//
                // Instantiate a new quiz object. //
                //--------------------------------//
                quiz = new NormalizedQuiz(schemaType, quizKey, quizItems, imgSrc, praise, numItems);
            }
        } else {
            console.warn("QuizFactory.createQuiz: unknown quiz: ", quizKey);
        }
    }
    return quiz;
}
QuizFactory.prototype.getPraise = function(quizJson) {
    let praise = undefined;
    if (quizJson.hasOwnProperty("praise")) {
        praise = quizJson.praise
    }
    return praise;
}
QuizFactory.prototype.getImgSrc = function(quizJson) {
    let imgSrc = "";
    if (quizJson.hasOwnProperty("imgSrc")) {
        imgSrc = quizJson.imgSrc;
    }
    return imgSrc;
}

//--------------------------------------------------------------------------//
// This class takes in quizzes of various schema types and normalizes them  //
// so consumption by clients is simplified down to one interface.           //
//--------------------------------------------------------------------------//

function NormalizedQuiz(schemaType, quizName, quizItems, imgSrc, praise, numItems) {
    this.quizName = quizName;
    this.quizItems = this.normalizeSchema(schemaType, quizItems);
    this.imgSrc = (imgSrc) ? imgSrc : this.defaultImgSrc;
    this.praise = (praise) ? praise : this.defaultPraise;
    if (numItems) {
        this.numItems = (numItems < this.quizItems.length) ? numItems : this.quizItems.length;
    } else {
        this.numItems = this.quizItems.length;
    }
    this.quizItems.length = this.numItems;
    this.index = 0;
    this.shuffleOnReset = true;
}
